using ll = long long;

constexpr ll inf = 1E18;
constexpr int mod = 1e9 + 7, maxn = 2e5 + 5;

void solve() {
    int n, x;
    cin >> n >> x;
    ll ans = 0;
    for (int a = 1; a <= min(n, x); a++) {
        for (int b = 1; a * b <= n and a + b <= x; b++) {
            ll c = min((n - a * b) / (a + b), x - a - b);
            ans += c;
        }
    }
    cout << ans << '\n';
}

int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    int tt = 1;
    cin >> tt;
    while (tt--) {
        solve();
    }
}