void solve() {
    int n;
    cin >> n;
    string s;
    cin >> s;
    map<char, string> mp;
    mp['4'] = "223";
    mp['6'] = "53";
    mp['8'] = "7222";
    mp['9'] = "3327";

    string ans;
    for (char c : s) {
        if ((c - '0') <= 1) continue;
        if (mp.count(c)) {
            ans += mp[c];
        } else {
            ans += c;
        }
    }
    sort(rall(ans));
    cout << ans << "\n";
}

