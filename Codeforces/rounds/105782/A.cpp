using ll = long long;

constexpr ll inf = 1E18;
constexpr int mod = 1e9 + 7, maxn = 2e5 + 5;

void solve() {
    ll a, b, c, x, y;
    cin >> a >> b >> c >> x >> y;

    ll mx = 0;
    for (ll i = 0; i <= a; i++) {
        for (ll j = 0; j <= b; j++) {
            ll cost = i * x + j * y;
            if (cost <= c) {
                mx = max(mx, i + j);
            }
        }
    }

    cout << mx << '\n';
}

int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    int tt = 1;  // cin >> tt;
    while (tt--) {
        solve();
    }
}